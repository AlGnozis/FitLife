print("Привет!")
print(
    "Добро пожаловать в фитнес-бот FitLife — "
    "ваш персональный ассистент по здоровью. "
    "\nОн поможет вам рассчитать индекс массы тела "
    "(ИМТ) и норму потребления воды.",
)
print("=" * 80)


# 1. Знакомство с пользователем
# Сохраняем имя пользователя (строка)
user_name = input("Как вас зовут? ")

# Сохраняем возраст пользователя и преобразуем в целое число (int)
user_age = int(input("Сколько вам лет? "))


# 2. Сбор данных для расчётов
# Ввод веса и преобразование в число с плавающей точкой (float)
user_weight = float(input("Введите ваш вес (в кг): "))

# Ввод роста и преобразование в число с плавающей точкой (float)
user_height = float(input("Введите ваш рост (в метрах, например 1.75): "))


# 3. Логика расчетов
# Формула ИМТ: масса тела (кг) ÷ (рост в метрах)²

# Константа: рекомендуемый объём воды — 30 мл на 1 кг массы тела
WATER_PER_KG = 30

# Константа: количество миллилитров в одном литре
WATER_IN_LITERS = 1000

# Расчёт ИМТ с округлением до 1 знака после запятой
bmi = user_weight / (user_height ** 2)

# Расчёт рекомендуемого объёма воды: вес (кг) × 30 мл
water_ml = user_weight * WATER_PER_KG

# Перевод в литры (для удобства восприятия)
water_l = water_ml / WATER_IN_LITERS


# 4. Вывод красивого результата
print("\n" + "=" * 50)
print("ВАШИ РЕЗУЛЬТАТЫ")
print("=" * 50)
print(f"Имя: {user_name}")
print(f"Возраст: {user_age} лет")
print(f"Ваш Индекс Массы Тела: {bmi:.1f}")
print(f"Рекомендуемая норма воды: {water_l:.1f} л. в день")
print("=" * 50)
print("\nРасчет окончен. Будьте здоровы!")
